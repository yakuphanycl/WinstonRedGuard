def _normalize_error_type(value: str | None, *, ok: bool) -> str | None:


























    if ok:


























        return "none"


























    if value is None:


























        return "error"


























    v = str(value).strip().lower()


























    aliases = {


























        "validation": "validation_error",


























        "io": "io_error",


























        "unknown": "unknown_error",


























        "render": "render_error",


























    }


























    return aliases.get(v, v or "error")
















































































