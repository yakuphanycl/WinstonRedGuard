def _qc_check_audio_stream(mp4_path: str) -> dict:
    """Check if mp4 has an audio stream. Never raises.
    Returns:
      { ok: bool, has_audio: bool, error?: str, details?: dict }
    """
    try:
        import os
        if not mp4_path or not os.path.exists(mp4_path):
            return {"ok": False, "has_audio": False, "error": f"missing mp4: {mp4_path}"}

        ffok = _ffprobe_ok()
        if not ffok.get("ok"):
            return {"ok": False, "has_audio": False, "error": ffok.get("error", "ffprobe not available")}

        import subprocess, json
        # Query streams: codec_type only, JSON output
        p = subprocess.run(
            ["ffprobe", "-v", "error", "-select_streams", "a",
             "-show_entries", "stream=index,codec_name,codec_type,channels,sample_rate",
             "-of", "json", mp4_path],
            capture_output=True, text=True
        )

        if p.returncode != 0:
            err = (p.stderr or p.stdout or "").strip()
            return {"ok": False, "has_audio": False, "error": err[:2000] if err else f"ffprobe rc={p.returncode}"}
        try:
            data = json.loads(p.stdout or "{}")
        except Exception:
            data = {}
        streams = data.get("streams") or []
        has_audio = len(streams) > 0
        return {"ok": True, "has_audio": has_audio, "details": {"audio_streams": streams}}
    except Exception as e:
        return {"ok": False, "has_audio": False, "error": str(e)[:2000]}
